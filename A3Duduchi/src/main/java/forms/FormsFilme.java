    private void SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarActionPerformed
// TODO add your handling code here:
        String titulo = TXTtitulo.getText();
        String diretor = TXTdiretor.getText();
        int ano = Integer.parseInt(TXTano.getText());
        String genero = TXTgenero.getText();
        double duracao = Double.parseDouble(TXTduracao.getText());
        String idioma = TXTidioma.getText();
        String pais = TXTpais.getText();
        String assistido = TXTassistido.getText();
        double avaliacao = Double.parseDouble(TXTavaliacao.getText());
        
        Filme filme = new Filme();
        filme.setTitulo(titulo);
        filme.setDiretor(diretor);
        filme.setAnoLancamento(ano);
        filme.setGenero(genero);
        filme.setDuracao(duracao);
        filme.setIdioma(idioma);
        filme.setPaisOrigem(pais);
        filme.setAssistido(assistido);
        filme.setAvaliacao(avaliacao);
        
        FilmeDAO filmeDAO = new FilmeDAO();
        filmeDAO.inserir(filme);
   
        TXTtitulo.setText("");
        TXTdiretor.setText("");
        TXTano.setText("");
        TXTgenero.setText("");
        TXTduracao.setText("");
        TXTidioma.setText("");
        TXTpais.setText("");
        TXTassistido.setText("");
        TXTavaliacao.setText("");
        
        
    }//GEN-LAST:event_SalvarActionPerformed

