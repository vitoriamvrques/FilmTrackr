    private void BTNbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNbuscarActionPerformed
        // TODO add your handling code here:
        int idPesquisa = Integer.parseInt(TXTbuscar.getText());
        FilmeDAO filmeDAO = new FilmeDAO();
        Filme filme = filmeDAO.getFilme(idPesquisa);
        if(filme == null){
            JOptionPane.showMessageDialog(this, "Filme não encontrado");
        }
        else{
            TXTID.setText(String.valueOf(filme.getId()));
            TXTtitulo.setText(filme.getTitulo());
            TXTdiretor.setText(filme.getDiretor());
            TXTano.setText(String.valueOf(filme.getAnoLancamento()));
            TXTgenero.setText(filme.getGenero());
            TXTduracao.setText(String.valueOf(filme.getDuracao()));
            TXTidioma.setText(filme.getIdioma());
            TXTpais.setText(filme.getPaisOrigem());
            TXTassistido.setText(filme.getAssistido());
            TXTavaliacao.setText(String.valueOf(filme.getAvaliacao()));
        
        }
        
    }//GEN-LAST:event_BTNbuscarActionPerformed

